def multiplicar(num1, num2):
    """
    Funcion que multiplica dos numeros
    Argumentos:
    numero1 (int)
    numero2 (int)
    Retorna la multiplicacion de los parametros dados.

    >>> multiplicar(2,3)
    6
    >>> multiplicar(2,4)
    5
    """
    return num1 * num2


print(f"El resultado de multiplicar es igual a {multiplicar(6,12)}")
